namespace NToolbox.Media
{
	/// <summary>
	/// Defines Audio Codecs.
	/// </summary>
	public enum AudioCodec
	{
		/// <summary>
		/// MPEG 1 Layer III Codec (MP3).
		/// </summary>
		MPEG_1_Layer_III,

		/// <summary>
		/// MPEG 2 Layer III Codec (MP3).
		/// </summary>
		MPEG_2_Layer_III,

		/// <summary>
		/// MPEG 2.5 Layer III Codec (MP3).
		/// </summary>
		MPEG_2_5_Layer_III,

		/// <summary>
		/// MPEG-4 Advanced Audio Coding (AAC).
		/// </summary>
		AAC_Plus,

		/// <summary>
		/// MPEG-4 High Efficiency Advanced Audio Coding (HE-AAC).
		/// </summary>
		AAC_Plus_High,

		/// <summary>
		/// Ogg.
		/// </summary>
		OGG,

		/// <summary>
		/// Windows Media Audio (WMA).
		/// </summary>
		WMA
	}
}